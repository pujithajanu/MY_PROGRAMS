#6.IMPLEMENTATION_OF_LIST_FUNCTIONS
lista=[1,4,6,7,0]
print('lista:',lista)
print('length:',len(lista))
print('max element:',max(lista))
print('min element:',min(lista))
print('sum:',sum(lista))
print('any:',any(lista))
print('all:',all(lista))
print('sorted:',sorted(lista))
print('reversed:',list(reversed(lista)))
del(lista)
print("list deleted!")