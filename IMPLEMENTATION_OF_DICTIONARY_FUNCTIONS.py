#7.IMPLEMENTATION_OF_DICTIONARY_FUNCTIONS
dicta={1:'pooja',4:'ram',6:'sita',7:'lakshman',0:'tana'}
print('dicta:',dicta)
print('length:',len(dicta))
print('max element:',max(dicta))
print('min key:',min(dicta))
print('sum:',sum(dicta))
print('any:',any(dicta))
print('all:',all(dicta))
print('sorted:',sorted(dicta))
print('reversed:',list(reversed(dicta)))
del(dicta)
print("dict deleted")